from abc import ABC, abstractmethod
from typing import Annotated

from openai import (
    AuthenticationError,
    BadRequestError,
    ConflictError,
    InternalServerError,
    NotFoundError,
    PermissionDeniedError,
    RateLimitError,
    UnprocessableEntityError,
)
from pydantic import BaseModel, Field

from app.config.settings import Settings


class LLMValidationError(Exception):
    """Raised when the LLM response doesn't match the expected structure."""


class LLMExceptionError(Exception):
    """
    Maps possible errors received regarding LLM API calls to messages that are more readable.
    """

    def __init__(self, original_exception: Exception) -> None:
        self.original_exception = original_exception
        self.message = self._map_error(original_exception)
        super().__init__(self.message)

    def _map_error(self, e: Exception) -> str:
        exception_map = {
            BadRequestError: "Something went wrong on our side. Please try again shortly.",
            UnprocessableEntityError: "The LLM couldn't understand the request. Could you try asking in a different way?",
            AuthenticationError: "Something went wrong on our side. Please try again shortly.",
            PermissionDeniedError: "Something went wrong on our side. Please try again shortly.",
            NotFoundError: "Something went wrong on our side. Please try again shortly.",
            ConflictError: "There was a temporary conflict while processing your request. Please try again.",
            RateLimitError: "Your request was rate limited. Please wait a moment and try again.",
            InternalServerError: "Something went wrong on our side. Please try again shortly.",
        }
        for error_type, message in exception_map.items():
            if isinstance(e, error_type):
                return message
        return "Something unexpected happened. Please try again."


class CriterionResult(BaseModel):
    """
    Represents the user defined criteria contained in the rubric.
    These criteria are what the LLM bases its evaluation upon.

    Attributes:
        criterion_name (str): The name of the criterion. E.g. "Politeness"
        score (int): To what degree the LLM judges the model_output fulfills the criterion on a scale of 1-4.
        reasoning (str): The LLM's reasoning behind the assigned score.
    """

    criterion_name: str
    reasoning: str
    score: Annotated[int, Field(gt=0, lt=5)]


class LLMResponse(BaseModel):
    """
    Represents the response of the LLM judge. Necessary to impose structure on the LLM's response.

    Attributes:
        results (list[CriterionResult]): Contains a list of Criterion results. These describe the results of the evaluation.
    """

    results: list[CriterionResult]


class BaseProvider(ABC):
    """
    Abstract base class for all providers. A provider is for example Azure OpenAI.

    Is responsible for constructing an evaluation prompt, contacting the LLM and constructing and returning a structured response.
    """

    def __init__(self, settings: Settings) -> None:
        self.model = settings.llm.model

    async def generate_response(self, model_output: str, prompt: str, rubric: list[str]) -> LLMResponse:
        """
        Generate and validate an evaluation response using the given rubric.

        Args:
            model_output: The output generated by the LLM to evaluate.
            prompt: The original prompt used to generate the model output.
            rubric: A non-empty list of criteria used for evaluation.

        Returns:
            A validated LLMResponse object.

        Raises:
            LLMValidationError: If the rubric is empty, the LLM response is invalid, or validation fails.
        """
        if len(rubric) < 1:
            raise LLMValidationError("rubric must contain at least one criterion.")

        response = await self._generate_response(model_output, prompt, rubric)

        if response is None:
            raise LLMValidationError("LLM returned an empty or unparseable response")

        self.validate_response(response, rubric)

        return response

    @abstractmethod
    async def _generate_response(self, model_output: str, prompt: str, rubric: list[str]) -> LLMResponse | None:
        """
        Generate an LLM evaluation response.

        This method must be implemented by subclasses and should return a parsed
        LLMResponse or None if the response cannot be parsed.

        Args:
            model_output: The output generated by the LLM to evaluate.
            prompt: The original prompt used to generate the model output.
            rubric: Evaluation criteria.

        Returns:
            An LLMResponse object, or None if the response cannot be generated or parsed.
        """

    @staticmethod
    def build_user_prompt(model_output: str, prompt: str, rubric: list[str]) -> str:
        """
        Construct a structured prompt for evaluating an LLM response.

        The prompt includes the original user question, the system-generated
        answer, and a numbered list of evaluation criteria specified in the rubric.

        Args:
            model_output: The response generated by the LLM to be evaluated.
            prompt: The original user prompt that produced the model output.
            rubric: A list of evaluation criteria used to assess the response.

        Returns:
            A formatted string prompt ready to be sent to an LLM for evaluation.
        """

        return f"""

        Now here are the inputs.

        USER QUESTION: {prompt}
        SYSTEM ANSWER: {model_output}
        CRITERIA:
            {"\n\t".join([f"\t{i + 1}. {crit}" for i, crit in enumerate(rubric)])}

        Provide your evaluation.

        """

    @staticmethod
    def validate_response(response: LLMResponse, rubric: list[str]) -> None:
        """
        Validate that the LLM response matches the expected rubric.

        Ensures that 1) The number of results matches the number of rubric criteria.
        and 2) Each rubric criterion is present exactly once in the response.

        Args:
            response: The parsed LLM response containing evaluation results.
            rubric: The list of expected evaluation criteria.

        Raises:
            LLMValidationError: If the number of results does not match the rubric,
            or if the criteria names in the response do not exactly match the rubric.
        """

        if len(response.results) != len(rubric):
            raise LLMValidationError(f"Expected {len(rubric)} criteria, got {len(response.results)}")

        returned_names = {r.criterion_name for r in response.results}
        expected_names = set(rubric)
        if returned_names != expected_names:
            raise LLMValidationError(f"Criteria mismatch... expected {expected_names}, got {returned_names}")

    @abstractmethod
    async def check_health(self) -> None:
        """Raise an exception if the provider is unavailable."""